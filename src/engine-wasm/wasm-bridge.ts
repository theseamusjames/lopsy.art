let initPromise: Promise<void> | null = null;
let initError: unknown = null;

export async function initWasm(): Promise<void> {
  if (initError) throw initError;
  if (!initPromise) {
    initPromise = init()
      .then(() => {})
      .catch((err) => {
        initError = err;
        initPromise = null;
        throw err;
      });
  }
  await initPromise;
}

